# 编写程序，定义2个float型变量并赋值，如果第一个数大于10.1，且第2个数小于20.0,打印两数之和
numb1 = 12.0
numb2 = 15.1
if numb1 > 10.1 and numb2 < 20.0:
    # print(numb1 + numb2)
    print(f"{numb1}  + {numb2} = {numb1 + numb2}")
