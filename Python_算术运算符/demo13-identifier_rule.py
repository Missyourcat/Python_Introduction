# 标识符的命名规则
# 1.由26个英文字母大小写，0-9,_组成
num9_N = 100
# num1~_N = 101
# 2.数字不可以开头
# 1num = 1
# 3.不可以使用关键字，但能包含关键字
# if = 500
my_if = 60
# 4.Python区分大小写
n = 700
N = 700
print(f"n={n},N={N}")
# 5.标识符不能包含空格
