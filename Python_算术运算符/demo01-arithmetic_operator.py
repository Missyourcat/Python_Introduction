# 算术运算符的使用
# /,//,%,**
# 对于除号/,返回结果是小数
print(10 / 3)
# 对于取整除//，返回商的整数部分(并且向下取整)
print(10 // 3)
print(-9 // 2)
print(-10 // 3)
# 当对一个数取模时，对应的运算公式: a % b = a - a // b * b
print(10 % 3)
print(-10 % 3)  # 分析-10 % 3 = -10 - (-10) // 3 * 3
print(10 % -3)
print(-10 % -3)

print(2 ** 5)
